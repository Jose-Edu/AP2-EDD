package atvd2;

public class Song {

    public String titulo;
    public String artista;
    public String album;
    public int segundos;

    public Song(String titulo, String artista, String album, int segundos) {

        this.titulo = titulo;
        this.artista = artista;
        this.album = album;
        this.segundos = segundos;

    }


}
